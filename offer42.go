/***********************************************************************************************************
输入一个整型数组，数组中的一个或连续多个整数组成一个子数组。求所有子数组的和的最大值。

要求时间复杂度为O(n)。
************************************************************************************************************
*/

func maxSubArray(nums []int) int {

    var dup int = 0
    var max int = nums[0]

    for _, v := range nums {

        if dup > 0 {
            dup += v
        } else {
            dup = v
        }

        if dup > max {
            max = dup
        }

    }

    return max

}