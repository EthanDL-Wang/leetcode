/***********************************************************************************************************
不使用运算符 + 和 - ​​​​​​​，计算两整数 ​​​​​​​a 、b ​​​​​​​之和。
************************************************************************************************************
*/


func getSum(a int, b int) int {


    if a == 0 {
        return b
    }

    if b == 0 {
        return a
    }

    var answer int = 0

    for i := 0; i < 64; i++ {
        
        if (a >> i) & 0x1 == 1 && (b >> i) & 0x1 == 1 {
            answer |= 0x1 << (i+1)
        } else if a >> i & 0x1 == 0 && b >> i & 0x1 == 0 {
            //donothing
        } else {
            if answer >> i & 0x1 == 1 {
                answer |= 0x1 << (i+1)
                answer &= ^(0x1 << i)
            } else {
                answer |= 0x1 << i
            }
        }
    }

    return answer

}