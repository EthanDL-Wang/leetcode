/***********************************************************************************************************
给你两个非负整数 low 和 high 。请你返回 low 和 high 之间（包括二者）奇数的数目。
************************************************************************************************************
*/

func getOdds(num int) int {
    count := num /2

    if num % 2 == 1 {
        count++
    }

    return count
}


func countOdds(low int, high int) int {

    var flag int 

    if low % 2 == 1 {
        flag = 1
    } 

    return getOdds(high) - getOdds(low) + flag
}