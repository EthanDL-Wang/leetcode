/***********************************************************************************************************
给定一个整数数组，找出总和最大的连续数列，并返回总和。
************************************************************************************************************
*/

func maxSubArray(nums []int) int {

    var dup int = 0
    var max int = nums[0]

    for _, v := range nums {

        if dup > 0 {
            dup += v
        } else {
            dup = v
        }

        if dup > max {
            max = dup
        }

    }

    return max

}