/***********************************************************************************************************
给你一个正方形矩阵 mat，请你返回矩阵对角线元素的和。

请你返回在矩阵主对角线上的元素和副对角线上且不在主对角线上元素的和。
************************************************************************************************************
*/

func diagonalSum(mat [][]int) int {
    lenRow := len(mat)
    sum := 0


    for i := 0; i < lenRow; i++ {
        sum += mat[i][i]

        if lenRow-1-i == i {
            continue
        } 

        sum += mat[lenRow-1-i][i]
    }

    return sum
}