/***********************************************************************************************************
给定一个无序的整数数组，找到其中最长上升子序列的长度。
************************************************************************************************************
*/




func lengthOfLIS(nums []int) int {

    dp := make([]int, len(nums))

    result := 0

    for i := 0; i < len(nums); i++ {
        dp[i] = 1

        for j := 0; j < i; j++ {

            if nums[j] < nums[i] {
                if dp[i] < dp[j] + 1 {
                    dp[i] = dp[j] + 1
                }                
            }
        }

        if result < dp[i] {
            result = dp[i]
        }

        //fmt.Println(dp)
    }



    return result


}